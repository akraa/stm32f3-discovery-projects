void TIM6_DAC_IRQHandler(void)
{
  if((TIM6->SR & TIM_SR_UIF ) == TIM_SR_UIF)
  {
    if(cont == 100) 
      cont = 0;
    TIM6->SR = 0; //Resetting
    DAC->DHR12R1 = ((int)(5140*sin(0.063*cont+8264)) & 0xFFF); //Amplitude 5140 , phase 8264. We have to & with 0xFFF because we are working on 12-bit right aligned.
    risultati[cont] = DAC->DHR12R1;
    campioni[cont] = DAC->DOR1;
    cont++;
  }
  
}

void EXTI0_IRQHandler(void)
{
  while((GPIOA->IDR & GPIO_IDR_0) == GPIO_IDR_0);
  EXTI->PR |= EXTI_PR_PR0; //Resetting the pending bit
  a++;
  if(a ==3) a = 0;
  prescaler = (int)pow(10,a);
  TIM6->PSC = (prescaler * 3199);
  
}

